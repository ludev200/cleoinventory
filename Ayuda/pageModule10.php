        <strong>10 - SISTEMA</strong>
    </h3>
    <p id="94">
        Este módulo comprende la configuración general y gestión del sistema, empezando con la restauración y copias de seguridad del sistema 
        donde se presenta un botón que inicia la ejecución de la herramienta externa <span hook="93" class="hooker">CLEO: Asistente Técnico</span>, seguido de la 
        sección de Identificación de documentos donde es posible modificar la cabecera de la empresa (nombre, RIF, dirección), logo, 
        sello y firma. Finalizando con unas opciones extra como el nombre o símbolo de la moneda nacional, preguntas de seguridad, unidades 
        de medida o colores del sistema.
    </p>
    <h4 id="95">10.1 - ELEMENTOS DE LA PANTALLA INICIAL DEL MÓDULO SISTEMA</h4><span id="indice89"></span>
    <ul id="96">
        <li id="97"><b>A - Botón de asistente técnico: </b>inicia la herramienta externa <span class="hooker" hook="93">CLEO: Asistente Técnico</span>.</li>
        <li id="98"><b>B - Identificación de documentos: </b>incluye la opción de cambiar el contenido de la cabecera de los documentos, los sellos 
        y la firma.</li>
        <li id="99"><b>C - Opciones extra: </b>contiene opciones extras como la modificación de la moneda nacional, las preguntas de seguridad 
        o las unidades de medida.</li>
        <li id="100"><b>D - Colores del sistema: </b>permite cambiar los colores del sistema entre cuatro opciones predeterminadas.</li>
        <li id="101"><b>E - Menú lateral: </b>contiene accesos directo para cada una de las secciones.</li>
    </ul>
    <img src="../Imagenes/manual/sistema_1.png">
    <h4 id="93">10.2 - CLEO: ASISTENTE TÉCNICO</h4><span id="indice90"></span>
    <p id="92">
        Esta es una herramienta externa, desarrollada con el fin de brindar soporte al usuario a la hora de gestionar la base de datos. 
        Permite exportar la base de datos, creando una copia de seguridad o punto de restauración que pueda ser utilizado en caso de desastre.
        Además, permite importar una copia de seguridad previamente creada.
    </p>
