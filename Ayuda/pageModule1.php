        <strong>2 - ALMACENES</strong>
    </h3>
    <p>
        CLEO INVENTORY fue diseñado para soportar una división del inventario en dos o más almacenes, cada almacén representa 
        una localidad geográfica distinta. Estos se gestionan en el módulo de almacenes, cada uno con un nombre, dirección e 
        inventario. Por lo tanto, la creación de al menos un almacén es el primer paso al hacer uso de este software. Este primer 
        almacén creado será interpretado como el <b id="23">almacén predeterminado</b>.
    </p>
    <h4>2.1 - ELEMENTOS DE LA PANTALLA INICIAL DEL MÓDULO ALMACENES</h4><span id="indice19"></span>
    <ul id="24">
        <li><b>A - Barra de búsqueda: </b> Permite buscar almacenes según su ID, nombre o dirección, agregando un filtro para 
        indicar el estado.</li>
        <li><b>B - Lista de resultados: </b> muestra los almacenes obtenidos como resultado de la búsqueda.</li>
        <li id="27"><b id="26">C - Menú lateral: </b> contiene el botón para agregar un nuevo almacén.</li>
    </ul>
    <img src="../Imagenes/manual/almacenes_1.png">
    <h4 id="25">2.2 REGISTRAR UN ALMACÉN</h4><span id="indice20"></span>
    <p>
        Accede al módulo almacenes, ubícate en el menú lateral y da clic al botón <b>Nuevo almacén</b>. Llena los campos del 
        formulario y seguidamente haz clic en el botón guardar. Nota: al registrar un almacén, es posible añadir un inventario 
        inicial, indicando los productos que contiene con su respectiva cantidad.
    </p>
    <ul>
        <li><b>A - Formulario: </b>En este el usuario ingresa un nombre referencial, la dirección y sus productos contenidos.</li>
        <li><b>B - Botones de acción:</b> Botón para salir o guardar el almacén con los datos indicados.</li>
        <li><b>C - Menú lateral: </b>Contiene el botón <b>Agregar producto</b> el cual despliega la ventana de selección de productos 
        donde el usuario puede añadir productos al inventario inicial del almacén. Además de un botón para limpiar el formulario.</li>
    </ul>
    <img src="../Imagenes/manual/almacenes_2.png">
    <h4>2.3 DETALLES DE UN ALMACÉN</h4><span id="indice21"></span>
    <p>
        En esta pantalla se presentan todos los detalles de un almacén, tal como el nombre, el ID, la dirección, el número de productos 
        almacenados y su fecha de creación. Además, indica la lista de productos almacenados y el historial de cambios.
    </p>
    <ul>
        <li><b>A - Lista de productos: </b>Muestra los productos y la cantidad almacenada en dicho almacén, y además indica la cantidad 
        total existente en el inventario.</li>
        <li><b>B - Historial de movimientos: </b>Indica los cambios de entrada y salida en dicho almacén y permite filtrar por
        tipo de movimiento.</li>
        <li><b>C - Menú lateral: </b>Contiene los botones para modificar y eliminar el almacén, además de un acceso directo para ir 
        a la sección de movimientos y otro para ir al inventario del sistema. Nota: la modificación del inventario de un 
        almacén se realiza a través de las compras, ventas o realizando un ajuste de inventario en módulo de <a href="31">inventario</a>.</li>
    </ul>
