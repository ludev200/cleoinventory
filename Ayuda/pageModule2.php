        <strong>6 - VENTAS</strong>
    </h3>
    <p>
        Las ventas son el principal método de salida de productos del inventario. CLEO INVENTORY permite al usuario la <a href="#65">creación 
        de cotizaciones</a>, proceso previo y obligatorio antes de confirmar una venta, es decir, una venta espera se considera una 
        cotización. <b id="64">Una cotización</b> es un presupuesto realizado para 
        conocer la estructura de costos previa a una venta. Una cotización puede ser confirmada, rechazada o puede considerarse vencida 
        en caso contar con un tiempo de vigencia el cual ya haya sido cumplido.
    </p>
    <h4>6.1 - ELEMENTOS DE LA PANTALLA INICIAL DEL MÓDULO VENTAS</h4><span id="indice58"></span>
    <ul>
        <li><b>A - Acción principal: </b>botones de llamada a la acción, incluye el botón de <a href="#65">crear nueva venta</a>, 
        <a href="#69">confirmar venta</a> y <a href="#68">rechazar venta</a>.
        <li><b>B - Lista de ventas: </b>incluye la lista de resultados donde se muestran las ventas y cotizaciones y una barra de 
        búsqueda que permite buscar ventas por ID, nombre o cliente además de filtrar por estado.</li>
        <li><b>C - Gráficos estadísticos: </b>presenta una gráfica de ventas por estado que compara las cotizaciones aceptadas (ventas), 
        rechazadas, en espera y vencidas. y otra gráfica comparativa entre los clientes más activos, es decir, con mayor número de ventas.</li>
        <li><b>D - Menú lateral: </b>contiene botones de acceso directo a <a href="#65">crear nueva venta</a>, buscar venta
        y gráficos estadísticos.</li>
    </ul>
    <img src="../Imagenes/manual/ventas_1.png">
    <h4 id="65">6.2 - CREAR UNA COTIZACIÓN</h4><span id="indice59"></span>
    <p>
        Accede al módulo ventas y da clic en <b>Nueva cotización</b>. Empieza indicando el cliente, puedes abrir la ventana de selección 
        de cliente dando clic en <b>Buscar cliente</b> o ir a <a href="#75">Registrar nuevo cliente</a> dando clic en <b>crear nuevo</b>, este 
        es un proceso opcional, ya que es posible la creación de cotizaciones sin indicar ningún cliente. Continua especificando los detalles 
        de tu cotización, escribe una descripción de la venta, si deseas establecer un límite de tiempo vigente para la misma, puedes hacerlo 
        escribiendo el número de días de vigencia o seleccionando la fecha de vencimiento, seguidamente indica los porcentajes a los 
        impuestos establecidos, así como el <b id="70">costo asociado al salario</b> (influyente únicamente en los <a href="#10">productos mano de obra</a>), 
        las utilidades y por último, el I.V.A. Finaliza agregando los productos a tu cotización con el botón <b>Agregar producto</b> el 
        cual despliega la ventana de selección de producto. Para guardar la cotización haz clic en el botón <b>Guardar</b>.
    </p>
    <ul>
        <li><b>A - Selección de cliente: </b>primera parte del formulario, permite indicar el cliente de la cotización, ya sea seleccionando 
        uno de los ya registrados en el sistema o abriendo la página de registro de cliente nuevo.</li>
        <li><b>B - Detalles de la venta: </b>segunda parte del formulario, incluye el nombre referencial o descripción del servicio, el tiempo de vigencia, el porcentaje 
        del costo asociado al salario, IVA y utilidades.</li>
        <li><b>C - Lista de productos: </b>tercera parte del formulario, indica los productos a cotizar y sus respectivas cantidades, clasificados por categoría.</li>
        <li><b>D - Totales: </b>muestra el precio acumulado de los productos cotizados junto a los subtotales, impuestos y el precio total.</li>
        <li><b>E - Botones de acción: </b>permite volver a la pantalla inicial del módulo de ventas o guardar la cotización.</li>
        <li><b>F - Menú lateral: </b>contiene el botón <b>Agregar producto</b> que despliega el módulo de selección de productos, un botón 
        para ir al manual y obtener ayuda sobre la creación de cotizaciones y otro para salir sin guardar.</li>
    </ul>
    <img src="../Imagenes/manual/ventas_2.png">
    <h4>6.3 - DETALLES DE UNA VENTA / COTIZACIÓN</h4><span id="indice60"></span>
    <p>
        Los detalles de una venta comprenden la descripción del servicio, estado de la venta, cliente, ID, tiempo de vigencia y 
        fecha de creación. Además del cuerpo de la cotización con sus productos materiales, equipo y mano de obra, finalizando con el 
        despliegue de totales. 
    </p>
    <ul>
        <li><b>A - Cuerpo de la venta: </b>contiene los productos cotizados con sus respectivas cantidades y la cuenta total de la venta.</li>
        <li><b>B - Botones de respuesta: </b>permite rechazar o confirmar la venta</li>
        <li id="66"><b id="67">C - Menú lateral: </b>contiene los botones para modificar o eliminar la cotización (solo disponibles para ventas en 
        espera), además de un botón para generar un reporte PDF indicando la cantidad y el tipo de moneda.</li>
    </ul>
    <img src="../Imagenes/manual/ventas_3.png">
    <h4 id="68">6.4 - RECHAZAR UNA COTIZACIÓN</h4><span id="indice61"></span>
    <p>
        Accede al módulo ventas y da clic en <b>Rechazar venta</b>. Utiliza el buscador de cotizaciones para seleccionar la cotización 
        a rechazar, seguidamente has clic en el botón <b>rechazar venta</b>. Rechazar una cotización la convierte en una venta rechazada, 
        y esta no podrá ser modificada o eliminada del sistema.
    </p>
    <ul>
        <li><b>A - Buscador de cotizaciones: </b>Permite buscar entre las cotizaciones del sistema filtrando por ID, descripción o RIF 
        del cliente.</li>
        <li><b>B - Cotización seleccionada: </b>Muestra un resumen de la cotización seleccionada en el buscador seguido de un botón
        para indicar el rechazo de la misma.</li>
        <li><b>C - Menú lateral: </b>Contiene un botón para volver a la <b>pantalla principal del módulo ventas</b>.</li>
    </ul>
    <img src="../Imagenes/manual/ventas_4.png">
    <h4 id="69">6.5 - CONFIRMAR UNA COTIZACIÓN</h4><span id="indice62"></span>
    <p>
        Accede al módulo ventas y da clic en <b>Confirmar venta</b>. Al registrar una venta, se realiza una entrada de productos en el 
        inventario. Esta venta puede preceder de una cotización creada previamente. CLEO INVENTORY es un software que cuenta con la 
        posibilidad de distribuir el inventario en múltiples almacenes. Por lo tanto, el proceso de compra se realiza siguiendo dos pasos:
    </p>
    <p>
        <b>PASO 1: </b>En este se indican los productos y las respectivas cantidades vendidas, lo cual se puede realizar de forma 
        individual o <b>importando una cotización</b> creada previamente. La descripción del servicio, los porcentajes, los productos 
        y el cliente pueden ser alterados si así se requiere.
    </p>
    <ul>
        <li><b>A - Detalles de la venta: </b>contiene la descripción de la venta, junto con sus porcentajes de <a href="#70">CAS</a>, 
        utilidades y I.V.A. Además del cliente.</li>
        <li><b>B - Lista de productos: </b>incluye los productos vendidos con sus respectivas cantidades.</li>
        <li><b>C - Botón de salida: </b>botón para salir sin guardar ningún cambio. Regresa a la <a href="#63">pantalla inicial 
        del módulo ventas</a>.</li>
        <li><b>D - Botones de selección: </b>incluye un botón para abrir la ventana de selección de cotización que permite importar 
        una cotización, un botón para abrir la ventana de selección de cliente y un botón para abrir la ventana de selección 
        de producto que permite agregar productos a la venta.</li>
        <li><b>E - Botón continuar: </b>permite proceder al <b>paso 2</b> del registro de venta.</li>
    </ul>
    <img src="../Imagenes/manual/ventas_5.png">
    <p>
        <b>PASO 2:</b> para concluir con el registro de una venta, se especifica la extracción de los productos, es decir, se indica 
        de cual almacén se extraerán los productos vendidos con sus respectivas cantidades. Nota: puede utilizarse el almacén 
        predeterminando para extraer de este todos los productos vendidos automáticamente.
    </p>
    <ul>
        <li><b>A - Lista de productos vendidos: </b>presenta los productos agregados a la venta con un botón para abrir la ventana de 
        selección de almacén, además de indicar la cantidad ya extraída en comparación con la cantidad vendida.</li>
        <li><b>B - Lista de productos extraídos: </b>muestra la extracción de los productos junto con el almacén de salida, la cantidad 
        de cada producto y la cantidad resultado luego de la extracción.</li>
        <li><b>C - Botón de volver: </b>permite volver al paso 1</li>
        <li><b>D - Botón de guardar: </b>guarda los cambios, confirmando así la venta en el sistema.</li>
    </ul>
