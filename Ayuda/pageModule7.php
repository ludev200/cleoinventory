        <strong>5 - PROVEEDORES</strong>
    </h3>
    <p>
        Un proveedor representa a la persona o empresa encargada de proveer o abastecer de materiales y equipo al inventario del sistema. En 
        CLEO INVENTORY el módulo de proveedores permite el registro y gestión de estos, con el fin de facilitar las compras mediante la 
        <a href="#43">creación de órdenes de compra</a>.
    </p>
    <h4>5.1 - ELEMENTOS DE LA PANTALLA INICIAL DEL MÓDULO PROVEEDORES</h4><span id="indice51"></span>
    <ul>
        <li><b>A - Lista de proveedores: </b>permite buscar proveedores por RIF (ID) o razón social (nombre) indicando también el orden en 
        el que se muestran los resultados, ya sea por RIF o nombre. Los proveedores que coinciden con la búsqueda se muestran en la lista 
        de resultados junto con el número de productos proveídos y un acceso directo a su respectivo perfil detallado.</li>
        <li><b>B - Menú lateral: </b>contiene el botón para <b>registrar un nuevo proveedor</b>.</li>
    </ul>
    <img src="../Imagenes/manual/proveedores_1.png">
    <h4 id="55">5.2 - REGISTRAR UN PROVEEDOR</h4><span id="indice52"></span>
    <p>
        Accede al módulo proveedores y da clic en <b>Nuevo proveedor</b>. Completa el formulario, indica los productos proveídos de ser 
        necesario y da clic en <b>Guardar</b>. El registro de un proveedor es un proceso sencillo que solo requiere el RIF o cédula y 
        la razón social o nombre, además de la dirección, los otros datos son opcionales, aunque recomendables. Nota: la lista de 
        productos proveídos puede ser modificada posteriormente al registro del proveedor.
    </p>
    <ul>
        <li><b>A - Formulario: </b>contiene los campos solicitados para el registro del proveedor, incluye la información personal tal 
        como, la imagen,  el tipo de documento, cédula o RIF y razón social (nombre); la información de contacto tal como, dos números 
        de teléfono, correo electrónico y dirección; y la lista de productos proveídos.</li>
        <li><b>B - Botones de acción: </b>contiene el botón para volver al <a href="#54">inicio del módulo de proveedores</a> y el botón para 
        guardar.</li>
        <li><b>C - Menú lateral: </b>contiene un botón ir al manual y obtener ayuda sobre los proveedores, y otro para salir sin guardar.</li>
    </ul>
    <img src="../Imagenes/manual/proveedores_2.png">
    <h4>5.3 - PERFIL DETALLADO DE UN PROVEEDOR</h4><span id="indice53"></span>
    <p>
        Aquí se presentan los detalles de un proveedor, incluyendo el nombre y el RIF, además de su información de contacto como los 
        números de teléfono, correo electrónico y dirección. Además, presenta la lista de productos proveídos.
    </p>
    <ul>
        <li><b>A - Lista de productos: </b>presenta los productos proveídos junto con un acceso directo al perfil detallado de cada uno.</li>
        <li id="56"><b id="57">B - Menú lateral: </b>contiene un botón para modificar los datos del proveedor y un botón para eliminarlo.</li>
    </ul>
