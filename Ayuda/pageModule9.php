        <strong>9 - USUARIOS</strong>
    </h3>
    <p>
        En este módulo se crean y gestionan los usuarios, dedicados a las personas que tienen acceso al sistema. Existen tres niveles de 
        usuario: Contador, Analista y administrador. La única diferencia entre estos es los módulos a los que tienen acceso de forma 
        predeterminada. Los módulos a los que puede acceder cada usuario pueden ser modificados por el <b>usuario administrador</b> o 
        cualquier usuario con acceso al módulo usuarios.
    </p>
    <h4>9.1 - ELEMENTOS DE LA PANTALLA INICIAL DEL MÓDULO USUARIOS</h4><span id="indice81"></span>
    <ul>
        <li><b>A - Lista de usuarios: </b>presenta los usuarios del sistema junto con su nivel de perfil, estado y un acceso directo 
        a su perfil detallado.</li>
        <li><b>B - Menú lateral: </b>contiene el botón para crear un nuevo usuario.</li>
    </ul>
    <img src="../Imagenes/manual/usuarios_1.png">
    <h4 id="85">9.2 - CREAR UN USUARIO</h4><span id="indice82"></span>
    <p>
        Accede al módulo usuarios, ubícate en el menú lateral y da clic al botón <b>Crear nuevo usuario</b>. Llena los campos del 
        formulario y seguidamente haz clic en el botón guardar. El formulario se divide en tres partes; en la primera indica la información 
        personal requerida; en la segunda parte indica el nombre de usuario y la contraseña, estos dos datos serán utilizados para 
        iniciar sesión, además selecciona las preguntas y respuestas de recuperación; en la tercera y última parte del formulario, 
        selecciona los permisos del usuario.
    </p>
    <ul>
        <li><b>A - Datos personales: </b>primera parte del formulario, incluye el tipo de documento, la cédula, el nombre y el sexo.</li>
        <li><b>B - Datos de la cuenta y seguridad: </b>segunda parte del formulario, incluye el nombre de usuario, la contraseña y las 
        preguntas de recuperación.</li>
        <li><b>C - Perfil de usuario: </b>tercera parte del formulario, incluye el nivel de usuario y los módulos permitidos.</li>
        <li><b>D - Botones de acción: </b>contiene los botones para salir o guardar.</li>
        <li><b>E - Menú lateral: </b>incluye un acceso directo a información de los usuarios en el manual.</li>
    </ul>
    <img src="../Imagenes/manual/usuarios_2.png">
    <h4>9.3 - PERFIL DETALLADO DE UN USUARIO</h4><span id="indice83"></span>
    <p>
        Aquí se muestran los datos del usuario, incluyendo su nombre de usuario, nivel de usuario, cédula, nombre y sexo. Además, 
        se presentan los módulos a los que este tiene acceso y un historial de acciones realizadas por el usuario.
    </p>
    <ul>
        <li><b>A - Módulos permitidos: </b>indica los módulos permitidos por el usuario.</li>
        <li><b id="88">B - Historial de usuario: </b>muestra un registro de las acciones realizadas por el usuario.</li>
        <li id="86"><b id="87">C - Menú lateral: </b>contiene un botón para modificar los datos de un usuario y otro botón para inhabilitar al 
        usuario (o habilitarlo en caso contrario).</li>
    </ul>
