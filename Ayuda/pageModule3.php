        <strong>7 - CLIENTES</strong>
    </h3>
    <p>
        Los clientes pueden ser personas o empresas quienes contratan, adquieren o compran un servicio. En CLELO INVENTORY el módulo 
        clientes permite el registro y la gestión de estos, con el fin de facilitar el ingreso y gestión de la información de los 
        clientes de nuestras ventas.
    </p>
    <h4>7.1 - ELEMENTOS DE LA PANTALLA INICIAL DEL MÓDULO CLIENTES</h4><span id="indice71"></span>
    <ul>
        <li><b>A - Lista de clientes: </b>Contiene una barra de búsqueda que permite filtrar los clientes por RIF o razón social e 
        indicar el orden en el que se muestran, los resultados obtenidos se muestran en la lista de resultados, cada cliente con 
        un acceso directo a si perfil detallado.</li>
        <li><b>B - Menú lateral: </b>Contiene el botón para ir a <a href="#75">agregar un nuevo cliente</a>.</li>
    </ul>
    <img src="../Imagenes/manual/Clientes_1.png">
    <h4 id="75">7.2 - REGISTRAR UN CLIENTE</h4><span id="indice72"></span>
    <p>
        Accede al módulo clientes y da clic en <b>Nuevo Cliente</b>. Completa el formulario y da clic en <b>Guardar</b>. El registro 
        de un cliente es un proceso sencillo que solo requiere el RIF o cédula y la razón social o nombre, además de la dirección, los 
        otros datos son opcionales, aunque recomendables.
    </p>
    <ul>
        <li><b>A - Formulario: </b>contiene los campos solicitados para el registro del cliente, incluye la información personal tal 
        como, la imagen,  el tipo de documento, cédula o RIF y razón social (nombre), y la información de contacto tal como, dos números 
        de teléfono, correo electrónico y dirección.</li>
        <li><b>B - Botones de acción: </b>contiene el botón para volver al <a href="#74">inicio del módulo de clientes</a> y el botón para 
        guardar.</li>
        <li><b>C - Menú lateral: </b>contiene un botón para salir sin guardar.</li>
    </ul>
    <img src="../Imagenes/manual/Clientes_2.png">
    <h4>7.3 - PERFIL DETALLADO DE UN CLIENTE</h4><span id="indice73"></span>
    <p>
        Aquí se presentan los detalles del cliente, incluyendo el nombre y el RIF, además de su información de contacto como los 
        números de teléfono, correo electrónico y dirección. Además, presenta la lista de sus cotizaciones y ventas.
    </p>
    <ul>
        <li><b>A - Lista de ventas: </b>presenta las ventas y cotizaciones del cliente, además de indicar su estado, fecha de expiración 
        e incluir un acceso directo a la venta. Además, incluye un buscador que permite filtrar los resultados por ID, descripción o estado.</li>
        <li id="76"><b id="77">B - Menú lateral: </b>contiene un botón para modificar al cliente y un botón para eliminarlo.</li>
    </ul>
