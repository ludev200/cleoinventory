        <strong>8 - AUDITORÍA</strong>
    </h3>
    <p>
        CLEO INVENTORY guarda un registro de todos los cambios realizados en el sistema, estos cambios pueden ser visualizados 
        por todos los usuarios con acceso al módulo auditoria, donde se muestran los registros junto con la fecha de su realización 
        y el usuario responsable. Al dar clic en un registro, se selecciona y se nos muestra un cartel con la entidad afectada y un 
        acceso directo a su perfil detallado.
    </p>
    <h4>8.1 - ELEMENTOS DE LA PANTALLA INICIAL DEL MÓDULO AUDITORÍA</h4><span id="indice78"></span>
    <ul>
        <li><b>A - Barra de búsqueda: </b>permite buscar cambios en el sistema según su ID o descripción, además de filtrar por 
        tipo de movimiento, entidad, mes o año.</li>
        <li id="80"><b>B - Lista de resultados: </b>muestra los resultados de la búsqueda, incluyendo el tipo de entidad, la fecha del 
        cambio y el usuario responsable.</li>
        <li><b>C - Menú lateral: </b>contiene un botón para volver a <a href="">inicio del sistema</a>.</li>
        <li><b>D - Cartel de entidad: </b>contiene un acceso directo al perfil detallado de la entidad seleccionada en la lista 
        de resultados.</li>
    </ul>
